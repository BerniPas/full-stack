//Bucle for

//arreglos
let nombres = ["Juan", "Pedro", "Ana", "Maria", "Jose"];

console.log(nombres.length);

  //inicio                límite/condición       incremento
for(let inicio = 0; inicio < nombres.length; inicio++ ){
    console.log("El valor de la variables es " + nombres[inicio]);
}

